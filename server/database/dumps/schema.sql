CREATE TABLE `mm`.`users` (
 `id` INT UNSIGNED NOT NULL AUTO_INCREMENT,
 `first_name` VARCHAR(72) NOT NULL,
 `last_name` VARCHAR(72) NOT NULL,
 `password` VARCHAR(32) NOT NULL,
 `email` VARCHAR(112) NOT NULL,
 `created_at` DATETIME NULL DEFAULT CURRENT_TIMESTAMP,
 `updated_at` DATETIME NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
 `is_active` TINYINT NULL DEFAULT 1,
 `is_admin` TINYINT NULL DEFAULT 1,
 PRIMARY KEY (`id`),
 UNIQUE INDEX `email_UNIQUE` (`email` ASC));


 CREATE TABLE `mm`.`contacts` (
  `id` INT UNSIGNED NOT NULL AUTO_INCREMENT,
  `created_at` DATETIME NULL DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP,
  `updated_at` DATETIME NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `name` VARCHAR(112) NOT NULL,
  `email` VARCHAR(112) NULL DEFAULT NULL,
  `mobile` VARCHAR(16) NULL DEFAULT NULL,
  `address` VARCHAR(255) NULL DEFAULT NULL,
  `user_id` INT UNSIGNED NOT NULL,
  PRIMARY KEY (`id`)
);

